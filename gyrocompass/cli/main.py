@app.command()
def drift(
    repo: Annotated[Optional[str], typer.Option("--repo", "-r", help="Path to the repository root.")] = None,
    files: Annotated[Optional[list[str]], typer.Option("--files", "-f", help="Specific files to analyze (repeatable).")] = None,
    pr_number: Annotated[Optional[int], typer.Option("--pr", help="PR number (informational).")] = None,
    output: Annotated[OutputFormat, typer.Option("--output", "-o", help="Output format.")] = OutputFormat.table,
) -> None:
    """Detect architectural drift against the baseline .gyrostate.yaml."""
    _suppress_loguru()
    repo_path = _resolve_repo(repo)

    # Suppress decorative output for machine-readable formats (CI, GitHub Action).
    if output == OutputFormat.table:
        console.print()
        console.rule("[bold cyan]GyroCompass — Drift Detection[/]")
        console.print()

    # Load baseline
    baseline = _load_state(repo_path)
    rules = _load_rules(repo_path)

    # Run fresh analysis
    from gyrocompass.indexer import CodeIndexer

    indexer = CodeIndexer(repo_path)

    with Progress(
        SpinnerColumn(),
        TextColumn("[progress.description]{task.description}"),
        console=console,
        transient=True,
    ) as progress:
        # Always index the full working tree for the "current" state. A partial
        # index of only changed files would make every unchanged component look
        # "removed" (false 100% drift). `files` is passed to the detector only
        # to scope the rule check to what actually changed.
        task = progress.add_task("[cyan]Indexing repository…", total=None)

        def _cb(msg: str, cur: int, tot: int) -> None:
            progress.update(task, description=f"[cyan]{msg}", total=tot or None, completed=cur)

        current = indexer.index(progress_callback=_cb)

    if pr_number:
        current.metadata.serial = pr_number  # type: ignore[assignment]

    # Detect drift
    from gyrocompass.drift import DriftDetector

    detector = DriftDetector(baseline=baseline, rules=rules)
    report = detector.detect(current, changed_files=files)

    # Opportunistically enrich with call-graph blast radius (graph backend only;
    # no-op + silent when GRAPH_BACKEND=lite or Memgraph is unavailable).
    try:
        from gyrocompass.graph.enrich import enrich_drift

        report = enrich_drift(report, files or [], repo_path)
    except Exception:
        pass

    # Attach PR number if provided
    if pr_number:
        report.pr_number = pr_number

    # Persist drift into architectural memory (best-effort — never break drift output)
    try:
        from gyrocompass.memory import MemoryStore

        _mem = MemoryStore(repo_path=repo_path)
        try:
            _mem.record_drift_report(report, pr=pr_number)
        finally:
            _mem.close()
    except Exception:
        pass

    # Output
    if output == OutputFormat.json:
        console.print_json(report.model_dump_json(indent=2))
    elif output == OutputFormat.markdown:
        console.print(report.to_markdown())
    else:
        _print_drift_report(report)

    # Exit code: 1 if blocking issues detected
    if report.has_blocking_issues:
        raise typer.Exit(code=1)


def _print_drift_report(report) -> None:
    """Render a drift report as a rich table."""
    from gyrocompass.models import DriftSeverity

    # Summary panel
    score_pct = f"{report.drift_score:.0%}"
    if report.drift_score == 0.0:
        score_text = f"[bold green]{score_pct}[/]"
        border = "green"
    elif report.drift_score < 0.3:
        score_text = f"[bold yellow]{score_pct}[/]"
        border = "yellow"
    else:
        score_text = f"[bold red]{score_pct}[/]"
        border = "red"

    summary_lines = [
        f"[bold]Drift Score:[/] {score_text}",
        f"[bold]Issues:[/] {len(report.events)} total  "
        f"([bold red]{report.critical_count}[/] critical  "
        f"[bold orange3]{report.high_count}[/] high  "
        f"[bold yellow]{report.medium_count}[/] medium  "
        f"[bold blue]{report.low_count}[/] low)",
        "",
        f"[dim]{report.summary}[/]",
    ]
    console.print(
        Panel(
            "\n".join(summary_lines),
            title="[bold]Drift Summary[/]",
            border_style=border,
            padding=(1, 2),
        )
    )

    if not report.events:
        console.print("\n[bold green]No architectural drift detected.[/]\n")
        return

    # Events table
    table = Table(
        title="[bold]Drift Events[/]",
        show_header=True,
        header_style="bold cyan",
        border_style="dim",
        show_lines=True,
        expand=True,
    )
    table.add_column("Severity", width=10)
    table.add_column("Type", width=22)
    table.add_column("Title", ratio=2)
    table.add_column("Element / File", ratio=1)
    table.add_column("Suggestion", ratio=2)

    severity_order = ["critical", "high", "medium", "low", "info"]
    sorted_events = sorted(
        report.events,
        key=lambda e: severity_order.index(e.severity.value) if e.severity.value in severity_order else 99,
    )

    for event in sorted_events:
        sev = event.severity.value
        style = _SEVERITY_STYLE.get(sev, "")
        location = event.element or event.file or "—"
        if event.file and event.line:
            location = f"{event.file}:{event.line}"
        table.add_row(
            Text(sev.upper(), style=style),
            event.type.value.replace("_", " "),
            event.title,
            location,
            (event.suggested_fix or "—")[:120],
        )

    console.print()
    console.print(table)

    if report.has_blocking_issues:
        console.print(
            "\n[bold red]Blocking issues detected.[/] Resolve critical/high issues before merging.\n"
        )
    else:
        console.print("\n[bold yellow]Non-blocking issues found.[/] Review before merging.\n")


