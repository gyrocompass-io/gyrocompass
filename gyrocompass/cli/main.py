_AUDIT_CAT_STYLE = {
    "security": "bold red",
    "capability": "bold yellow",
    "tech_debt": "bold magenta",
}
_AUDIT_CAT_EMOJI = {"security": "🔴", "capability": "🟡", "tech_debt": "🟣"}


@app.command()
def audit(
    repo: Annotated[Optional[str], typer.Option("--repo", "-r", help="Path to the repository root.")] = None,
    output: Annotated[OutputFormat, typer.Option("--output", "-o", help="Output format.")] = OutputFormat.table,
    only: Annotated[Optional[list[str]], typer.Option("--only", help="Run only these scanners (secrets, pii, injection, auth, tests, ci, observability, dependencies).")] = None,
    fail_on: Annotated[str, typer.Option("--fail-on", help="Exit non-zero if findings of this severity or worse exist: critical|high|medium|low|none.")] = "none",
) -> None:
    """Audit a finished app for ship-readiness — secrets, PII, vuln deps, injection, missing tests/CI.

    Emits an agent-ready punch list you can paste into Claude Code / Cursor to fix.
    """
    _suppress_loguru()
    repo_path = _resolve_repo(repo)

    from gyrocompass.audit import AuditEngine

    engine = AuditEngine(repo_path)

    if output == OutputFormat.table:
        console.print()
        console.rule("[bold cyan]GyroCompass — Ship-Readiness Audit[/]")
        console.print()

    with console.status("[cyan]Scanning…[/]", spinner="dots") if output == OutputFormat.table else _nullctx():
        report = engine.run(only=only or None)

    if output == OutputFormat.json:
        console.print_json(report.model_dump_json(indent=2))
    elif output == OutputFormat.markdown:
        console.print(report.to_markdown())
    else:
        _print_audit_report(report)

    # Exit code policy
    sev_order = {"critical": 0, "high": 1, "medium": 2, "low": 3}
    if fail_on != "none" and fail_on in sev_order:
        threshold = sev_order[fail_on]
        worst = min((sev_order[f.severity.value] for f in report.findings), default=99)
        if worst <= threshold:
            raise typer.Exit(code=1)


def _nullctx():
    from contextlib import nullcontext

    return nullcontext()


def _print_audit_report(report) -> None:
    bs = report.by_severity
    header = (
        f"[bold]{len(report.findings)}[/] finding(s) across {report.files_scanned} files  •  "
        f"[red]{report.critical_count} critical[/]  "
        f"[orange3]{report.high_count} high[/]  "
        f"[yellow]{bs['medium']} medium[/]  "
        f"[blue]{bs['low']} low[/]"
    )
    if not report.findings:
        console.print(Panel("[bold green]✅ Ship-ready.[/] No issues found by the audit checklist.",
                            border_style="green", padding=(1, 2)))
        return
    verdict = ("[bold red]🚨 Not ship-ready — fix critical/high before deploy.[/]"
               if not report.is_ship_ready else
               "[yellow]⚠️ Review lower-severity items; nothing blocking.[/]")
    console.print(Panel(f"{header}\n\n{verdict}", title="[bold]Audit Summary[/]", border_style="cyan", padding=(1, 2)))
    console.print()

    table = Table(show_header=True, header_style="bold", expand=True)
    table.add_column("", width=3)
    table.add_column("Category", width=12)
    table.add_column("Check", width=22)
    table.add_column("Finding", overflow="fold")
    table.add_column("Location", width=22, overflow="fold")

    sev_rank = {"critical": 0, "high": 1, "medium": 2, "low": 3}
    for f in sorted(report.findings, key=lambda x: (sev_rank[x.severity.value], x.category.value)):
        cat = f.category.value
        loc = ""
        if f.file:
            loc = f.file + (f":{f.line}" if f.line else "")
        sev_dot = {"critical": "[red]●[/]", "high": "[orange3]●[/]", "medium": "[yellow]●[/]", "low": "[blue]●[/]"}[f.severity.value]
        table.add_row(
            sev_dot,
            f"[{_AUDIT_CAT_STYLE[cat]}]{_AUDIT_CAT_EMOJI[cat]} {cat.replace('_', ' ')}[/]",
            f"[dim]{f.id}[/]",
            f.message + (f"\n[green dim]→ {f.fix}[/]" if f.fix else ""),
            f"[dim]{loc}[/]",
        )
    console.print(table)
    console.print()
    console.print("[dim]Tip: [bold]gyro audit -o markdown[/] gives an agent-ready punch list to paste into Claude Code / Cursor.[/]")


