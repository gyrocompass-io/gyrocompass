    if rows is None and isinstance(answer, dict):
        rows = answer.get("results")
    rows = rows or []

    if cypher:
        console.print(f"[dim]{cypher}[/]")
        console.print()

    if not rows:
        console.print("[dim]No results.[/]")
        return

