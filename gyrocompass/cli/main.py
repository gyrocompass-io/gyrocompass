# ── Helpers ────────────────────────────────────────────────────────────────────


def _resolve_repo(repo: str | None) -> Path:
    """Return an absolute repo path, defaulting to CWD."""
    return Path(repo).resolve() if repo else Path.cwd()


def _load_state(repo_path: Path):
    """Load ArchitectureState from .gyrostate.yaml.  Exits with error if missing."""
    from gyrocompass.config import get_state_path

    state_path = get_state_path(repo_path)
    if not state_path.exists():
        console.print(
            Panel(
                f"[bold red]No baseline found.[/]\n\n"
                f"Expected: [bold]{state_path}[/]\n\n"
                "Run [bold cyan]gyro init[/] and then [bold cyan]gyro analyze --save[/] first.",
                title="[red]Error[/]",
                border_style="red",
            )
        )
        raise typer.Exit(code=1)

    from gyrocompass.models import ArchitectureState

    with state_path.open(encoding="utf-8") as fh:
        raw = yaml.safe_load(fh)
    return ArchitectureState.model_validate(raw)


def _load_rules(repo_path: Path):
    """Load Rules from .gyrorules.yaml.  Returns empty Rules if file absent."""
    from gyrocompass.config import get_rules_path
    from gyrocompass.models import Rules

    rules_path = get_rules_path(repo_path)
    if not rules_path.exists():
        return Rules()
    with rules_path.open(encoding="utf-8") as fh:
        raw = yaml.safe_load(fh) or {}
    return Rules.model_validate(raw)


def _load_config(repo_path: Path) -> dict:
    """Load project config.yaml.  Returns {} if not found."""
    from gyrocompass.config import GYROCONFIG_FILE

    cfg_path = repo_path / GYROCONFIG_FILE
    if not cfg_path.exists():
        return {}
    with cfg_path.open(encoding="utf-8") as fh:
        return yaml.safe_load(fh) or {}


def _suppress_loguru() -> None:
    """Remove the default loguru handler so only rich output appears in CLI."""
    logger.remove()


